package com.lkm.asking.entity;

public class BoxAnswer {
    private Integer boxAid;
    private Integer boxId;
    private String boxAcontent;
    private String boxAtime;
    private String username;

    public Integer getBoxAid() {
        return boxAid;
    }

    public void setBoxAid(Integer boxAid) {
        this.boxAid = boxAid;
    }

    public Integer getBoxId() {
        return boxId;
    }

    public void setBoxId(Integer boxId) {
        this.boxId = boxId;
    }

    public String getBoxAcontent() {
        return boxAcontent;
    }

    public void setBoxAcontent(String boxAcontent) {
        this.boxAcontent = boxAcontent;
    }

    public String getBoxAtime() {
        return boxAtime;
    }

    public void setBoxAtime(String boxAtime) {
        this.boxAtime = boxAtime;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
